function spawnCAP()

	local CAPFlight = nil
	local SpawnFlight = nil

	--build initial ownership table
	airbaseOwnership = {}


	for k,v in pairs(caucasusMatrix) do

		airbaseOwnership[k] = Airbase.getByName(k):getCoalition()

	end

	--check which airfields are in conflict with each other (opposing coalition airbases next to each other)
	for k,v in pairs(caucasusMatrix) do

		activeAirbase = AIRBASE:FindByName(k)
		neighbouringBases = {}
		neighbouringBases = caucasusMatrix[k]

		for i = 1, #neighbouringBases do

			spawn = false
			enemy = true
			country = "CJTF_RED"
			--check if there are any friendly or enemy airbase next to each other, and also check for neutral airbases
			if (airbaseOwnership[k] == 1 and airbaseOwnership[neighbouringBases[i]] == 2) or (airbaseOwnership[k] == 2 and airbaseOwnership[neighbouringBases[i]] == 1) or (airbaseOwnership[k] == 1 and airbaseOwnership[neighbouringBases[i]] == 0) or (airbaseOwnership[k] == 2 and airbaseOwnership[neighbouringBases[i]] == 0) then
				spawn = true
			end

			if airbaseOwnership[k] == 2 then
				enemy = false
				country = "CJTF_BLUE"
			end

			if spawn == true then
				--spawn CAP flight
				aircraftType = CAPSelect[math.random(1,#CAPSelect)]
				trigger.action.outText("Spawning a flight of " .. aircraftType,10)--debug
				activeSpawns[k] = SPAWN:New(aircraftType)
				CAPFlight = activeSpawns[k]

				CAPFlight:InitCountry(country)
				--SpawnFlight = CAPFlight:SpawnAtAirbase(activeAirbase)
				SpawnFlight = CAPFlight:Spawn()


			end


		end

	end

end

